def calculate_flames(n1, n2):
    name1 = n1.lower().replace(" ", "")
    name2 = n2.lower().replace(" ", "")

    for char in name1[:]:
        if char in name2:
            name1 = name1.replace(char, "", 1)
            name2 = name2.replace(char, "", 1)

    count = len(name1) + len(name2)
    res = ["Friends", "Lovers", "Affectionate", "Marriage", "Enemies", "Siblings"]

    if count > 0:
        while len(res) > 1:
            split_index = (count % len(res) - 1)
            if split_index >= 0:
                right = res[split_index + 1:]
                left = res[:split_index]
                res = right + left
            else:
                res = res[:len(res) - 1]
        return res[0]
    return "Soulmates"

